/*
 * final：最终的
 * 1. final 可以修饰的结构：类、变量、方法
 * 2. final 修饰一个类：
 *      - 该类不能被其他类继承
 *      - 如 String类、System类、StringBuffer类
 * 3. final 方法：
 *      - 方法不能重写
 *      - 例如 Object类的 getClass() 方法
 * 4. final 变量:
 *      - 此时变量变为常量
 *      - 显式初始化、代码块中初始化、构造器中初始化
 *      - 修饰局部变量——常量
 *      - 形式参数中为final，则调用此方法时，该形参为常量，不可修改
 * static final ——> 修饰属性：全局常量
 */
